def extract_text_from_pdf(pdf_file):
    try:
        with fitz.open(pdf_file) as doc:
            text = "".join(page.get_text("text") for page in doc)
        if not text.strip():
            logging.info("No text extracted directly; attempting OCR.")
            with tempfile.NamedTemporaryFile(suffix=".pdf", delete=False) as tmp_file:
                with fitz.open(pdf_file) as doc:
                    with open(tmp_file.name, 'wb') as f:
                        f.write(doc.write())
                images = convert_from_path(tmp_file.name, poppler_path='/usr/bin/')
                text = "\n".join(pytesseract.image_to_string(img, config='--psm 6') for img in images)
            os.remove(tmp_file.name)
        text = re.sub(r'\n\s*\n+', '\n', text)
        text = re.sub(r'[^\x00-\x7F]+', ' ', text)
        text = re.sub(r'\s+', ' ', text)
        text = re.sub(r'-\n', '', text)
        text = re.sub(r'\n', ' ', text)
        logging.debug(f"Extracted text (first 200 chars): {text[:200]}...")
        return text.strip() or "No text extracted from any page."
    except Exception as e:
        logging.error(f"PDF extraction failed: {str(e)}")
        return f"Error: {str(e)}"

def extract_entities(text):
    doc = nlp(text)
    entities = defaultdict(list)
    section_pattern = re.compile(r"^[IVXLC]+\.\s+[A-Za-z]+|^[0-9]+\.[0-9]*\s+[A-Za-z]+")
    for ent in doc.ents:
        if ent.label_ in ["PERSON", "ORG", "GPE", "LOC"] and not section_pattern.match(ent.text):
            entities[ent.text].append(ent.label_)
    merged_entities = {}
    entity_keys = sorted(entities.keys(), key=len, reverse=True)
    for ent in entity_keys:
        labels = entities[ent]
        matched = False
        for existing_ent in list(merged_entities.keys()):
            if ent.lower() in existing_ent.lower() or existing_ent.lower() in ent.lower():
                merged_entities[existing_ent]["labels"].extend(labels)
                matched = True
                break
        if not matched:
            merged_entities[ent] = {"labels": labels}
    unique_entities = {}
    for ent, data in merged_entities.items():
        label = max(set(data["labels"]), key=data["labels"].count, default="ENTITY")
        if label == "LOC" and any(keyword in ent.lower() for keyword in ["roadster", "quotes", "inspiration"]):
            label = "ENTITY"
        wiki_page = wiki.page(ent)
        wiki_id = None
        try:
            if wiki_page.summary:
                wiki_id = wiki_page.fullurl
        except (KeyError, AttributeError):
            wiki_id = None
        unique_entities[ent] = {"type": label, "wiki_id": wiki_id}
        logging.debug(f"Entity extracted: {ent} ({label})")
    logging.info(f"Extracted and linked {len(unique_entities)} entities")
    return unique_entities

def extract_relationships(text, entities):
    doc = nlp(text)
    relationships = set()
    verb_categories = {
        "born": "BORN_IN", "live": "LIVES_IN", "reside": "LIVES_IN", "grow": "GREW_UP_IN",
        "die": "DIED_IN", "marry": "MARRIED_TO", "work": "WORKS_AT", "employ": "EMPLOYED_BY",
        "found": "FOUNDED", "lead": "LEADS", "manage": "MANAGES", "join": "JOINED",
        "own": "OWNS", "create": "CREATED", "invent": "INVENTED", "attend": "ATTENDED",
        "study": "STUDIED_AT", "graduate": "GRADUATED_FROM", "teach": "TAUGHT_AT",
        "meet": "MET", "visit": "VISITED", "travel": "TRAVELED_TO", "speak": "SPOKE_AT",
        "collaborate": "COLLABORATED_WITH", "fight": "FOUGHT_WITH", "mention": "MENTIONED_WITH",
        "write": "WROTE", "publish": "PUBLISHED", "influence": "INFLUENCED", "inspire": "INSPIRED",
        "buy": "BOUGHT", "sell": "SOLD", "acquire": "ACQUIRED", "represent": "REPRESENTS",
        "participate": "PARTICIPATED_IN", "support": "SUPPORTED", "oppose": "OPPOSED",
        "discover": "DISCOVERED"
    }
    patterns = [
        (r"(?P<subj>[A-Za-z\s]+)\s+was\s+born\s+in\s+(?P<obj>[A-Za-z\s]+)", "BORN_IN"),
        (r"(?P<subj>[A-Za-z\s]+)\s+founded\s+(?P<obj>[A-Za-z\s]+)", "FOUNDED"),
        (r"(?P<subj>[A-Za-z\s]+)\s+works\s+(at|for)\s+(?P<obj>[A-Za-z\s]+)", "WORKS_AT"),
        (r"(?P<subj>[A-Za-z\s]+)\s+is\s+from\s+(?P<obj>[A-Za-z\s]+)", "FROM"),
        (r"(?P<subj>[A-Za-z\s]+)\s+created\s+(?P<obj>[A-Za-z\s]+)", "CREATED"),
    ]
    logging.info(f"Processing {len(list(doc.sents))} sentences for relationships")
    for pattern, rel_type in patterns:
        for match in re.finditer(pattern, text, re.IGNORECASE):
            subj = match.group("subj").strip()
            obj = match.group("obj").strip()
            subj_ent = next((e for e in entities.keys() if subj.lower() in e.lower() or e.lower() in subj.lower()), None)
            obj_ent = next((e for e in entities.keys() if obj.lower() in e.lower() or e.lower() in obj.lower()), None)
            if subj_ent and obj_ent and subj_ent != obj_ent:
                relationships.add((subj_ent, obj_ent, rel_type))
                logging.debug(f"Pattern-based relationship: {subj_ent} → {obj_ent} ({rel_type})")
    for sent in doc.sents:
        sent_entities = [e for e in entities.keys() if e.lower() in sent.text.lower()]
        if len(sent_entities) < 2:
            logging.debug(f"Skipping sentence with <2 entities: {sent.text[:50]}...")
            continue
        verb_found = False
        for token in sent:
            if token.pos_ == "VERB":
                subj = obj = prep_obj = None
                prep = None
                for child in token.children:
                    if child.dep_ in ["nsubj", "nsubjpass", "compound", "agent"]:
                        subj = child.text
                    elif child.dep_ in ["dobj", "attr", "pobj"]:
                        obj = child.text
                    elif child.dep_ == "prep":
                        prep = child.text
                        for grandkid in child.children:
                            if grandkid.dep_ in ["pobj"]:
                                prep_obj = grandkid.text
                subj_ent = next((e for e in sent_entities if subj and (subj.lower() in e.lower() or e.lower() in subj.lower())), None)
                obj_ent = next((e for e in sent_entities if (obj and (obj.lower() in e.lower() or e.lower() in obj.lower())) or
                               (prep_obj and (prep_obj.lower() in e.lower() or e.lower() in prep_obj.lower()))), None)
                if subj_ent and obj_ent and subj_ent != obj_ent:
                    verb = token.lemma_
                    rel = verb_categories.get(verb, None)
                    if not rel:
                        if prep:
                            if prep == "at" and entities.get(obj_ent, {}).get("type") in ["ORG", "GPE"]:
                                rel = "WORKS_AT" if verb in ["work", "serve", "speak"] else "EVENT_AT"
                            elif prep == "in" and entities.get(obj_ent, {}).get("type") in ["GPE", "LOC"]:
                                rel = "LIVES_IN" if verb in ["live", "reside", "stay"] else "EVENT_IN"
                            elif prep == "with":
                                rel = "COLLABORATED_WITH" if verb in ["work", "collaborate", "partner"] else "ASSOCIATED_WITH"
                            else:
                                rel = f"{verb.upper()}_{prep.upper()}"
                        else:
                            rel = f"{verb.upper()}_TO"
                    relationships.add((subj_ent, obj_ent, rel))
                    logging.debug(f"Dependency-based relationship: {subj_ent} → {obj_ent} ({rel})")
                    verb_found = True
        if not verb_found and len(sent_entities) >= 2:
            for i in range(len(sent_entities) - 1):
                e1, e2 = sent_entities[i], sent_entities[i + 1]
                if e1 != e2:
                    e1_type = entities.get(e1, {}).get("type")
                    e2_type = entities.get(e2, {}).get("type")
                    if e1_type == "PERSON" and e2_type in ["ORG", "GPE"]:
                        rel = "RELATED_TO"
                        relationships.add((e1, e2, rel))
                        logging.debug(f"Sentence co-occurrence relationship: {e1} → {e2} ({rel})")
    if not relationships:
        logging.info("No relationships found; inferring based on entity types.")
        entity_list = list(entities.items())
        for i, (ent1, attrs1) in enumerate(entity_list):
            for ent2, attrs2 in entity_list[i+1:]:
                if ent1 != ent2:
                    type1, type2 = attrs1["type"], attrs2["type"]
                    if type1 == "PERSON" and type2 == "ORG":
                        relationships.add((ent1, ent2, "FOUNDED"))
                        logging.debug(f"Inferred relationship: {ent1} --FOUNDED--> {ent2}")
                    elif type1 == "PERSON" and type2 == "GPE":
                        relationships.add((ent1, ent2, "BORN_IN"))
                        logging.debug(f"Inferred relationship: {ent1} --BORN_IN--> {ent2}")
                    elif type1 == "ORG" and type2 == "GPE":
                        relationships.add((ent1, ent2, "LOCATED_IN"))
                        logging.debug(f"Inferred relationship: {ent1} --LOCATED_IN--> {ent2}")
    G_temp = nx.DiGraph()
    for entity in entities.keys():
        G_temp.add_node(entity)
    for e1, e2, rel in relationships:
        G_temp.add_edge(e1, e2, relation=rel)
    isolated_nodes = list(nx.isolates(G_temp))
    if isolated_nodes:
        logging.info(f"Found {len(isolated_nodes)} isolated nodes: {isolated_nodes}")
        if G_temp.number_of_nodes() > len(isolated_nodes):
            non_isolated_nodes = [n for n in G_temp.nodes if n not in isolated_nodes]
            degree_dict = dict(G_temp.degree(non_isolated_nodes))
            central_node = max(degree_dict, key=degree_dict.get, default=non_isolated_nodes[0]) if degree_dict else non_isolated_nodes[0]
            if "Elon Musk" in G_temp.nodes:
                central_node = "Elon Musk"
            elif "Conclusion Elon Musk" in G_temp.nodes:
                central_node = "Conclusion Elon Musk"
            elif "Elon Musk Inspiration" in G_temp.nodes:
                central_node = "Elon Musk Inspiration"
            for node in isolated_nodes:
                node_type = entities.get(node, {}).get("type", "ENTITY")
                central_type = entities.get(central_node, {}).get("type", "ENTITY")
                if node_type == "PERSON" and central_type in ["ORG", "ENTITY"]:
                    rel = "ASSOCIATED_WITH"
                elif node_type == "ORG" and central_type == "PERSON":
                    rel = "FOUNDED_BY"
                elif node_type in ["GPE", "LOC"] and central_type == "PERSON":
                    rel = "VISITED"
                elif node_type == "ENTITY" and central_type == "PERSON":
                    rel = "CREATED_BY"
                else:
                    rel = "RELATED_TO"
                relationships.add((node, central_node, rel))
                logging.debug(f"Connected isolated node {node} to {central_node} with relation {rel}")
    if not relationships:
        logging.warning("No relationships extracted after all methods.")
    logging.info(f"Extracted {len(relationships)} unique relationships")
    return list(relationships)

def build_knowledge_graph(entities, relationships):
    G = nx.DiGraph()
    for entity, attrs in entities.items():
        G.add_node(entity, type=attrs["type"], wiki_id=attrs["wiki_id"])
    for e1, e2, rel in relationships:
        weight = 1.5 if rel != "RELATED_TO" else 0.8
        G.add_edge(e1, e2, relation=rel, weight=weight)
    logging.info(f"Built graph with {G.number_of_nodes()} nodes and {G.number_of_edges()} edges.")
    return G

def generate_graph_image(G):
    plt.figure(figsize=(16, 12))
    pos = nx.spring_layout(G, k=1.5, iterations=100, seed=42)
    node_degrees = dict(G.degree())
    max_degree = max(node_degrees.values(), default=1)
    node_sizes = [800 + 400 * (node_degrees[node] / max_degree) for node in G.nodes]
    node_colors = {"PERSON": "#a3cfbb", "ORG": "#80bdff", "GPE": "#ff9999", "LOC": "#ffcc99", "ENTITY": "#d9d9d9"}
    nx.draw_networkx_nodes(G, pos, node_color=[node_colors[G.nodes[n]["type"]] for n in G.nodes], node_size=node_sizes, alpha=0.9)
    nx.draw_networkx_edges(G, pos, edge_color="#6b7280", width=1.5, arrows=True, arrowstyle="-|>", arrowsize=15)
    nx.draw_networkx_labels(G, pos, font_size=10, font_weight="bold", verticalalignment="center", horizontalalignment="center")
    edge_labels = {(u, v): d["relation"] for u, v, d in G.edges(data=True)}
    nx.draw_networkx_edge_labels(G, pos, edge_labels=edge_labels, font_size=8, label_pos=0.5, rotate=False)
    plt.tight_layout()
    plt.margins(0.1)
    buf = io.BytesIO()
    plt.savefig(buf, format="png", dpi=150, bbox_inches="tight")
    plt.close()
    buf.seek(0)
    return Image.open(buf)

def generate_questions(graph, num_questions, blooms_level):
    if not graph or graph.number_of_nodes() == 0:
        return "No entities or relationships in the graph to generate questions from."
    bloom_templates = {
        "remembering": {
            "PERSON": ["Who is {entity}?", "Where was {entity} born?", "When did {entity} start their career?"],
            "ORG": ["What is {entity}?", "Where is {entity} headquartered?", "When was {entity} founded?"],
            "GPE": ["Where is {entity} located?", "What is the capital of {entity}?", "When was {entity} established?"],
            "LOC": ["Where is {entity} situated?", "What is {entity} known for?", "When was {entity} discovered?"],
            "ENTITY": ["What is {entity}?", "Where is {entity} commonly found?", "When was {entity} introduced?"],
            "RELATION": ["Who did {source} collaborate with in {target}?", "What connects {source} to {target}?", "Where did {source} meet {target}?"]
        },
        "understanding": {
            "PERSON": ["What role does {entity} play?", "Why is {entity} influential?", "How is {entity} described in the text?"],
            "ORG": ["What is the mission of {entity}?", "How does {entity} operate?", "Why was {entity} established?"],
            "GPE": ["What is {entity} famous for?", "Why is {entity} culturally significant?", "How is {entity} governed?"],
            "LOC": ["What makes {entity} a notable location?", "Why is {entity} important?", "How is {entity} characterized?"],
            "ENTITY": ["What does {entity} represent in the context?", "Why is {entity} significant?", "How is {entity} defined?"],
            "RELATION": ["What is the relationship between {source} and {target}?", "Why are {source} and {target} connected?", "How do {source} and {target} interact?"]
        },
        "applying": {
            "PERSON": ["How might {entity} address a challenge?", "What could {entity} contribute to {target}?"],
            "ORG": ["How can {entity} be applied in a new project?", "What would happen if {entity} expands to {target}?"],
            "GPE": ["How could {entity} be used in a travel plan?", "What benefits does {entity} offer to {target}?"],
            "LOC": ["How might {entity} be utilized in modern times?", "What could {entity} provide to {target}?"],
            "ENTITY": ["How can {entity} be applied in a scenario?", "What role can {entity} play in {target}?"],
            "RELATION": ["How does the connection between {source} and {target} benefit a project?", "What can {source} offer to {target}?"]
        },
        "analyzing": {
            "PERSON": ["Why did {entity} influence {target}?", "How does {entity} compare to {target} in impact?"],
            "ORG": ["What factors contribute to {entity}'s success?", "How does {entity} differ from {target} in operations?"],
            "GPE": ["Why is {entity} distinct from {target}?", "What causes {entity}'s prominence?"],
            "LOC": ["How does {entity} relate to {target} geographically?", "Why is {entity} significant compared to {target}?"],
            "ENTITY": ["What makes {entity} different from {target}?", "How does {entity} impact {target}?"],
            "RELATION": ["Why are {source} and {target} linked in this way?", "How does the relationship between {source} and {target} affect outcomes?"]
        }
    }
    blooms_level = blooms_level.lower()
    if blooms_level not in bloom_templates:
        return f"Invalid Bloom's Taxonomy level: {blooms_level}. Choose from: Remembering, Understanding, Applying, Analyzing."
    possible_questions = []
    for entity, attrs in graph.nodes(data=True):
        entity_type = attrs.get("type", "ENTITY")
        templates = bloom_templates[blooms_level].get(entity_type, bloom_templates[blooms_level]["ENTITY"])
        for template in templates:
            try:
                question = template.format(
                    entity=entity,
                    target=random.choice(list(graph.nodes())) if "{target}" in template else ""
                )
                if count_verbs(question) == 1:
                    possible_questions.append(question)
            except (KeyError, ValueError) as e:
                logging.warning(f"Failed to format template for entity {entity}: {e}")
                continue
    for source, target, data in graph.edges(data=True):
        templates = bloom_templates[blooms_level]["RELATION"]
        for template in templates:
            try:
                question = template.format(source=source, target=target)
                if count_verbs(question) == 1:
                    possible_questions.append(question)
            except (KeyError, ValueError) as e:
                logging.warning(f"Failed to format relation template for {source} -> {target}: {e}")
                continue
    if not possible_questions:
        return "No questions could be generated from the graph."
    num_to_select = min(num_questions, len(possible_questions))
    if num_to_select < num_questions:
        logging.warning(f"Only {len(possible_questions)} unique questions available, requested {num_questions}.")
    selected_questions = random.sample(possible_questions, num_to_select)
    output = "\n".join([f"{i+1}. {q}" for i, q in enumerate(selected_questions)])
    if num_to_select < num_questions:
        output += f"\n\nNote: Only {num_to_select} unique questions could be generated due to limited graph content."
    return output

def process_knowledge_graph(pdf_file, num_questions, blooms_level):
    text = extract_text_from_pdf(pdf_file)
    entities = extract_entities(text)
    relationships = extract_relationships(text, entities)
    graph = build_knowledge_graph(entities, relationships)
    graph_img = generate_graph_image(graph) if graph.number_of_nodes() > 0 else None
    questions = generate_questions(graph, num_questions, blooms_level) if graph else "No questions generated."
    return graph_img, questions

