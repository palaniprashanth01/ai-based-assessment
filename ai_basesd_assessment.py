class CustomSummarizer:
    def __init__(self):
        self.tokenizer = AutoTokenizer.from_pretrained("facebook/bart-large-cnn")
        self.model = AutoModelForSeq2SeqLM.from_pretrained("facebook/bart-large-cnn")
        self.device = "cuda" if torch.cuda.is_available() else ("mps" if torch.backends.mps.is_available() else "cpu")
        self.model.to(self.device)
    
    def __call__(self, text, max_length=130, min_length=30, do_sample=False):
        inputs = self.tokenizer([text], max_length=1024, return_tensors="pt", truncation=True).to(self.device)
        summary_ids = self.model.generate(
            inputs["input_ids"], 
            max_length=max_length, 
            min_length=min_length, 
            do_sample=do_sample
        )
        summary_text = self.tokenizer.batch_decode(summary_ids, skip_special_tokens=True)[0]
        return [{'summary_text': summary_text}]

