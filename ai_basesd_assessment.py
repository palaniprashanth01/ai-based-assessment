def filter_text_by_topic(text: str, topic: str) -> str:
    if not topic:
        return text
    paragraphs = text.split('\n\n')
    filtered_paragraphs = [p for p in paragraphs if topic.lower() in p.lower()]
    if not filtered_paragraphs:
        return "No content found for the specified topic."
    return '\n\n'.join(filtered_paragraphs)

def translate_text(text: str, target_language: str) -> str:
    translator = GoogleTranslator(source='en', target=target_language)
    max_chars = 5000
    translated_chunks = []
    sentences = nltk.sent_tokenize(text)
    current_chunk = ""
    for sentence in sentences:
        sentence = sentence.encode('utf-8', errors='replace').decode('utf-8')
        if len(current_chunk) + len(sentence) + 1 <= max_chars:
            current_chunk += sentence + " "
        else:
            try:
                translated_chunk = translator.translate(current_chunk.strip())
                if translated_chunk:
                    translated_chunks.append(translated_chunk)
                else:
                    translated_chunks.append(current_chunk.strip())
            except Exception as e:
                logging.error(f"Translation error: {e}")
                return f"Error translating to {target_language}: {e}"
            current_chunk = sentence + " "
    if current_chunk.strip():
        try:
            translated_chunk = translator.translate(current_chunk.strip())
            if translated_chunk:
                translated_chunks.append(translated_chunk)
            else:
                translated_chunks.append(current_chunk.strip())
        except Exception as e:
            logging.error(f"Final chunk translation error: {e}")
            return f"Error translating to {target_language}: {e}"
    translated_text = " ".join(translated_chunks)
    return translated_text

