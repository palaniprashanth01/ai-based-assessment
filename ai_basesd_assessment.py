def read_pdf(file_path):
    text = ""
    with fitz.open(file_path) as pdf_document:
        for page_num in range(pdf_document.page_count):
            page = pdf_document.load_page(page_num)
            text += page.get_text("text")
    paragraphs = [para.strip() for para in text.split('\n\n') if para.strip()]
    return paragraphs if paragraphs else ["No readable text found in the PDF."]

def truncate_text(context, max_tokens=512):
    words = context.split()
    if len(words) > max_tokens:
        return ' '.join(words[:max_tokens])
    return context

def get_summary(paragraphs):
    combined_text = " ".join(paragraphs)
    truncated_context = truncate_text(combined_text)
    input_length = len(truncated_context.split())
    max_length = max(5, input_length // 2)
    min_length = max(3, max_length // 2)
    summarized_text = summarizer(truncated_context, max_length=max_length, min_length=min_length, do_sample=False)
    return summarized_text[0]['summary_text']

def get_keywords(summary_text):
    rake.extract_keywords_from_text(summary_text)
    keywords = rake.get_ranked_phrases()[:50]
    if len(keywords) < 10:
        doc = nlp(summary_text)
        additional_keywords = [
            token.text for token in doc
            if token.pos_ in ["NOUN", "ADJ", "VERB"] and len(token.text) > 2
            and token.text.lower() not in [kw.lower() for kw in keywords]
        ]
        keywords.extend(additional_keywords[:max(0, 10 - len(keywords))])
    keywords = [re.sub(r'[^a-zA-Z0-9\s]', '', kw).strip() for kw in keywords]
    return [kw for kw in keywords if kw and len(kw) > 2]

def detect_content_theme(keywords):
    motivational_indicators = {"inspire", "motivation", "goal", "success", "achieve", "resilience", "encourage", "aspire", "drive", "passion"}
    leadership_indicators = {"leader", "leadership", "team", "decision", "vision", "manage", "strategy", "guide", "direct", "influence"}

    keyword_set = set(word.lower() for kw in keywords for word in kw.split())
    motivational_score = len(keyword_set & motivational_indicators)
    leadership_score = len(keyword_set & leadership_indicators)

    if motivational_score > leadership_score and motivational_score > 0:
        return "motivational"
    elif leadership_score > 0:
        return "leadership"
    return "computing"

