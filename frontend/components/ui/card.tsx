import * as React from "react";
import { cn } from "@/lib/utils";

/**
 * Navigate card.
 * - Radius: radius.sm (25.15px) for the outer shell; nested elements use radius.xs.
 * - Surface: --card token (raised near-black).
 * - Border: border.strong (rgb(20,20,20)) at 1px.
 * - Long content: overflow-hidden so child media respect the radius.
 */
export const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-sm border border-border bg-card text-card-foreground overflow-hidden",
        className,
      )}
      {...props}
    />
  ),
);
Card.displayName = "Card";

export const CardHeader = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div ref={ref} className={cn("flex flex-col gap-1.5 px-6 pt-6 pb-3", className)} {...props} />
  ),
);
CardHeader.displayName = "CardHeader";

